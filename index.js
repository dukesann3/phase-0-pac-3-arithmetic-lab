function add(a,b){
    return a+b;
}

function subtract(a,b){
    return a-b;
}

function multiply(a,b){
    return a*b;
}

function divide(a,b){
    return a/b;
}

function increment(a){
    a++;
    return a;
}

function decrement(a){
    a--;
    return a;
}

function makeInt(n){
    n = parseInt(n,10);
    return n;
}

function preserveDecimal(n){
    n = parseFloat(n);
    return n;
}

